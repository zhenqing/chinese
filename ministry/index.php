            <h3>Campus Witnessing</h3>
            <p>A witness has a very crucial role within the history of God. Therefore, we need to understand what being a witness represents. A witness, before, was someone that was able to testify to the miraculous acts in the bible. Although, nowadays, a witness is someone who can testify to the love that God has shown through His truth, mercy, and grace. Furthermore, on a deeper level, a witness is someone that has received the grace, fully, and wishes to share that grace with others.<br /><br />Regardless of who we are, being a witness to Christ is vital in supporting the commission that God has appointed us to. We must witness in every circumstance of our lives, both publicly and privately, in every atmosphere always holding true to the model of our Lord, Jesus Christ.<br /><br />The mission of YD brings us to the college campuses of the world. A college can be defined as an institution bringing the world's brightest minds to test their areas of knowledge within this world and granting them degrees for their achievements, but within this environment the value of God's knowledge decreases. To be a witness on campus is to share our knowledge of Christ and to show the significance of His word. More importantly, we, as witnesses take part in something larger than people can imagine, and so should represent the value of who we are. </p>
