            <h3>Missionary Dispatch</h3>
            <p>The theology of missionary work is based in the scriptures from the beginning to the end. This is the revelation of the very nature of God and the Bible and part of the progression of the history of salvation. This is the record of human history for God's eternal purpose which brings him glory.</p>
            <p>Young Disciples of Jesus has a history of sending missionaries to various corners of the world from participating nations. By sending the missionaries, our goal is to establish presence within a nation and raise native student leaders to take charge of their own national campuses. We recruit missionary from within local fellowship branches or local universities and dispatch them for both short term and long term missions.</p>
            <p>For every missionary who is dispatched, the field administration would ensure the proper training and readiness were provided and mobilize financial and spiritual support for the mission. Also, making certain that proper communication can be kept from field to administration is a crucial element of the dispatch.</p>
            <h3>Short-Term Missions</h3>
            <p>To college students or ones who has an occupation restricted to a certain area who has interest in going on short-term missions, Young Disciples of Jesus offers opportunities in various nations for mission work from a period of 6 months to 3 year. The types of mission programs in this area includes summer programs for students, short-term programs fro retirees, professionals, and other skilled persons that can take time out of their normal routine.</p>
            <p>Many young people have first sensed their calling in missionary vocation by entering such program. Some have verified the direction of their lives. Through the experience, a new understanding of missions was gained and the new vision of mission gives a new perspective to their lives. </p> 
		<h3>Christian Tentmakers </h3>
            <p>The policy of Young Disciples of Jesus on missionary dispatch is the three-self policy: self-governing, self-propagating, and self-supporting. The mindset that is set to each region of mission financially is the principle of tent-making. Apostle Paul, when he went on wondering missions evangelizing in the areas of Macedonia and Minor Asia, took the initiative of using his profession as a tool for financing his ministry. Each mission field works on its own accord without dependency. The native leadership and missions are trained with the same approach.</p>
        <h3>Mission  Network</h3>
        <p>To maintain proper communication and report among mission fields across the world, the international and regional administrations have developed advanced global networking through the effective use of information technology.</p>
        <p>Through technology, reports on mission progress and communications are conduct on a standard basis to ensure the accountability of each mission field and to provide higher standard of service to certain fields when needed.</p>
        <p>The network is managed and moderated by a team to bring the fastest update and mission reports across the world. Effective and clear directives to mission work are essential in building a direct relationship and goals of each mission to the same vision. </p>
